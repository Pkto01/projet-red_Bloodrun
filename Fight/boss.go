// DisplayMonsterArt cherche l'art d'un monstre par son nom et l'affiche avec un style spécial.
func DisplayMonsterArt(monsterName string) {
	// 1. On récupère l'art ASCII correspondant au nom du monstre
	art, ok := monsterArt[monsterName]
	if !ok {
		// Si aucun art n'est trouvé pour ce monstre, on ne fait rien.
		return
	}

	// 2. On sépare l'art en plusieurs lignes
	lines := strings.Split(art, "\n")

	// 3. On parcourt chaque ligne pour l'afficher caractère par caractère
	for _, line := range lines {
		for _, char := range line {
			// Si le caractère n'est pas un espace, on l'affiche en noir sur fond blanc.
			if char != ' ' {
				fmt.Printf("\033[30;47m%c\033[0m", char) // Style: Noir sur Blanc
			} else {
				// Sinon, on affiche l'espace normalement.
				fmt.Printf("%c", char)
			}
		}
		// On passe à la ligne suivante dans le terminal une fois la ligne d'art terminée.
		fmt.Println()
	}
}
